T = int(input())
# 여러개의 테스트 케이스가 주어지므로, 각각을 처리합니다.
for test_case in range(T):
    result = 0
    # ///////////////////////////////////////////////////////////////////////////////////
    case = list(map(int, input().split()))
    for i in case:
        if i % 2 != 0:
            result += i
    print('#{}'.format(test_case + 1), result)
    # ///////////////////////////////////////////////////////////////////////////////////
